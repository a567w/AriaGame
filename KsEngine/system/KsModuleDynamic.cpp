/************************************************************************************************/
/*
 * コンストラクタ(初期化)
 * @param	path				パス名
 * @param	name				モジュール名
 * @param	pKsModuleManager		モジュールマネージャ
 */
/************************************************************************************************/
KsModuleDynamic::KsModuleDynamic( const KsString& fullPath, const KsString& name, KsModuleManager* pModuleManager )
	: KsModule( name, pModuleManager )
	, m_pLib( 0 )
	, m_fullPath( fullPath )
{
	m_pLib = (void*)LoadLibrary( m_fullPath.c_str() );
}

/************************************************************************************************/
/*
 * デストラクタ
 */
/************************************************************************************************/
KsModuleDynamic::~KsModuleDynamic()
{
	if( m_pLib )
	{
		FreeLibrary( (HINSTANCE)m_pLib );
	}
}

/************************************************************************************************/
/*
 * モジュール内のエクスポート関数のアドレスを取得します。
 * @param			name		エクスポート関数名
 * @return						モジュール内のエクスポート関数のアドレス
 */
/************************************************************************************************/
void* KsModuleDynamic::getProcAddress( const KsString& name )
{
	return (void*)GetProcAddress( (HINSTANCE)m_pLib, name.c_str() );
}



