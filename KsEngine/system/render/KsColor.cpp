const KsColor		KsColor::RED( 255, 0, 0, 0 );
const KsColor		KsColor::GREEN( 0, 255, 0, 0 );
const KsColor		KsColor::BLUE( 0, 0, 255, 0 );
const KsColor		KsColor::WHITE( 255, 255, 255, 0 );
const KsColor		KsColor::BLACK( 0, 0, 0, 0 );


/************************************************************************************************/
/*
 *
 */
/************************************************************************************************/
KsColor& KsColor::operator = ( const KsColorReal& refColor )
{
	KsInt32		inv = 255;

	set( ( KsByte )( inv*refColor.getRed() ), ( KsByte )( inv*refColor.getGreen() ), ( KsByte )( inv*refColor.getBlue() ), ( KsByte )( inv*refColor.getAlpha() ) );
	
	return ( *this );
}

