KsTexture::KsTexture()
{
	m_id        = 0;
	m_name[ 0 ] = '\0';
	m_numMipmap = 0;
	m_width     = 0;
	m_height    = 0;
	//m_format    = 0;
	m_pImage    = NULL;
}

KsTexture::~KsTexture()
{
	ksDELETE( m_pImage );
}

void KsTexture::destroy()
{
	/* 参照カウントを下げる */
	dec();
}