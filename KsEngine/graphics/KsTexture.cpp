/************************************************************************************************/
/*
 * コンストラクタ
 */
/************************************************************************************************/
KsTexture::KsTexture()
{
	m_name      = "";
	m_id        = 0;
	m_flags     = 0;
	m_numMipmap = 0;
	m_width     = 0;
	m_height    = 0;
	m_pNext     = NULL;
}

/************************************************************************************************/
/*
 * デストラクタ
 */
/************************************************************************************************/
KsTexture::~KsTexture()
{
}

/************************************************************************************************/
/*
 * テクスチャ名をセットする
 * @param		pFileName			テクスチャ名
 */
/************************************************************************************************/
void KsTexture::setName( const KsChar* pFileName )
{
	m_name = pFileName;
	
	m_id = KsCrc32( (KsUInt8*)pFileName );
}



