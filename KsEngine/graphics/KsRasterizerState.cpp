/************************************************************************************************/
/*
 * コンストラクタ
 */
/************************************************************************************************/
KsRasterizerState::KsRasterizerState()
{
	KsZeroMemory( &m_rasterizerState, sizeof(KS_RASTERIZER_DESC) );
}

/************************************************************************************************/
/*
 * コンストラクタ
 */
/************************************************************************************************/
KsRasterizerState::KsRasterizerState( const KS_RASTERIZER_DESC* pDepthStencilDesc )
{
	KsMemCopy( &m_rasterizerState, pDepthStencilDesc, sizeof(KS_RASTERIZER_DESC) );
}

/************************************************************************************************/
/*
 * コンストラクタ
 */
/************************************************************************************************/
