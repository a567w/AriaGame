/************************************************************************************************/
/*
 * ボーンを描画する
 */
/************************************************************************************************/
void KsBone::draw()
{
	if( m_pParent )
	{
		KsGraphics*	pGraphics = KsGraphics::getInstancePtr();

		pGraphics->drawLine( m_worldTransform.getTranslate(), m_pParent->getWorldTransform().getTranslate() );
	}
}

