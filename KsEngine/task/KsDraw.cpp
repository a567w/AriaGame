/************************************************************************************************/
/*
 * コンストラクタ
 */
/************************************************************************************************/
KsDraw::KsDraw()
	: m_drawID( 0 )
	, m_drawOrder( 0 )
	, m_drawPriority( 0 )
	, m_drawActive( 0 )
	, m_drawFlags( 0 )
	, m_pDrawPrev( 0 )
	, m_pDrawNext( 0 )
	, m_pDrawManager( 0 )
{
}

/************************************************************************************************/
/*
 * コンストラクタ
 * @param	order	描画オーダー
 */
/************************************************************************************************/
KsDraw::KsDraw( KsUInt32 order )
	: m_drawID( 0 )
	, m_drawOrder( order )
	, m_drawPriority( 0 )
	, m_drawActive( 0 )
	, m_drawFlags( 0 )
	, m_pDrawPrev( 0 )
	, m_pDrawNext( 0 )
	, m_pDrawManager( 0 )
{
}


/************************************************************************************************/
/*
 * デストラクタ
 */
/************************************************************************************************/
KsDraw::~KsDraw()
{
}

/************************************************************************************************/
/*
 * 破棄する(マネージャ側で削除する)
 */
/************************************************************************************************/
void KsDraw::destroyDraw()
{
	if( m_pDrawManager )
	{
		m_pDrawManager->destroyDraw( this );
	}
	else
	{
		delete this;
	}
}

