/************************************************************************************************/
/*
 * コンストラクタ
 */
/************************************************************************************************/
KsUpdate::KsUpdate()
	: m_updateID( 0 )
	, m_updateOrder( 0 )
	, m_updatePriority( 0 )
	, m_updateActive( 0 )
	, m_updateFlags( 0 )
	, m_pUpdatePrev( 0 )
	, m_pUpdateNext( 0 )
	, m_pUpdateManager( 0 )
{
}

/************************************************************************************************/
/*
 * コンストラクタ
 * @param	order	描画オーダー
 */
/************************************************************************************************/
KsUpdate::KsUpdate( KsUInt32 order )
	: m_updateID( 0 )
	, m_updateOrder( order )
	, m_updatePriority( 0 )
	, m_updateActive( 0 )
	, m_updateFlags( 0 )
	, m_pUpdatePrev( 0 )
	, m_pUpdateNext( 0 )
	, m_pUpdateManager( 0 )
{
}

/************************************************************************************************/
/*
 * デストラクタ
 */
/************************************************************************************************/
KsUpdate::~KsUpdate()
{
}

/************************************************************************************************/
/*
 * 破棄する(マネージャ側で削除する)
 */
/************************************************************************************************/
void KsUpdate::destroyUpdate()
{
	if( m_pUpdateManager )
	{
		m_pUpdateManager->destroyUpdate( this );
	}
	else
	{
		delete this;
	}
}


