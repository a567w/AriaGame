/************************************************************************************************/
/*
 * コンストラクタ
 */
/************************************************************************************************/
KsIKPositionConstraint::KsIKPositionConstraint()
{
	m_boneIndex = -1;
	m_start     = KsVector3d::ZERO;
	m_end       = KsVector3d::ZERO;
	m_current   = KsVector3d::ZERO;
}

/************************************************************************************************/
/*
 * デストラクタ
 */
/************************************************************************************************/
KsIKPositionConstraint::~KsIKPositionConstraint()
{
}

/************************************************************************************************/
/*
 * 更新処理
 * @param	gameTime	ゲーム更新時間
 */
/************************************************************************************************/
void KsIKPositionConstraint::update( KsReal gameTime )
{
}

/************************************************************************************************/
/*
 * 補間座標を算出する
 * @param	rate		補間レート
 */
/************************************************************************************************/
void KsIKPositionConstraint::interpolate( KsReal rate )
{
	m_current = KsVector3d::Lerp( m_start, m_end, rate );
}


