/************************************************************************************************/
/*
 * コンストラクタ
 * @param	pInputSystem	入力システム
 * @param	mouseIndex		パッドインデックス
 */
/************************************************************************************************/
KsGameMouse::KsGameMouse( KsInputSystem* pInputSystem, KsInt32 mouseIndex )
	: m_pInputSystem( pInputSystem )
	, m_mouseIndex( mouseIndex )
{
}

/************************************************************************************************/
/*
 * デストラクタ
 */
/************************************************************************************************/
KsGameMouse::~KsGameMouse()
{
}

/************************************************************************************************/
/*
 * 更新処理
 */
/************************************************************************************************/
void KsGameMouse::update()
{
}

/************************************************************************************************/
/*
 *
 */
/************************************************************************************************/
KsBool KsGameMouse::isLeftButtonDown() const
{
	return ( m_pInputSystem->getMouse()->getButtonTrg() & ksMOUSE_BUTTON_LEFT );
}

/************************************************************************************************/
/*
 *
 */
/************************************************************************************************/
KsBool KsGameMouse::isLeftButtonUp() const
{
	return ( m_pInputSystem->getMouse()->getButtonOff() & ksMOUSE_BUTTON_LEFT );
}

/************************************************************************************************/
/*
 *
 */
/************************************************************************************************/
KsBool KsGameMouse::isRightButtonDown() const
{
	return ( m_pInputSystem->getMouse()->getButtonTrg() & ksMOUSE_BUTTON_RIGHT );
}

/************************************************************************************************/
/*
 *
 */
/************************************************************************************************/
KsBool KsGameMouse::isRightButtonUp() const
{
	return ( m_pInputSystem->getMouse()->getButtonOff() & ksMOUSE_BUTTON_RIGHT );
}

/************************************************************************************************/
/*
 *
 */
/************************************************************************************************/
KsBool KsGameMouse::isWheel() const
{
	KsInt32	z = m_pInputSystem->getMouse()->getPosZ();

	return ( KsAbs( z ) > 1 );
}

/************************************************************************************************/
/*
* マウスのX座標を取得する
* @return			マウスのX座標
*/
/************************************************************************************************/
KsInt32 KsGameMouse::getPosX()      const
{
	return m_pInputSystem->getMouse()->getPosX();
}

/************************************************************************************************/
/**
* マウスのY座標を取得する
* @return			マウスのY座標
*/
/************************************************************************************************/
KsInt32 KsGameMouse::getPosY()      const
{
	return m_pInputSystem->getMouse()->getPosY();
}

/************************************************************************************************/
/**
* マウスのZ座標を取得する(ホイール)
* @return			マウスのZ座標
*/
/************************************************************************************************/
KsInt32 KsGameMouse::getPosZ()      const
{
	return m_pInputSystem->getMouse()->getPosZ();
}



