template<> KsGameConfigManager* KsSingleton< KsGameConfigManager >::m_pInstance = 0;

/************************************************************************************************/
/*
 * シングルトンの参照を取得
 * @return							シングルトンの参照
 */
/************************************************************************************************/
KsGameConfigManager& KsGameConfigManager::getInstance()
{  
	return (*m_pInstance);  
}

/************************************************************************************************/
/*
 * シングルトンのポインタを取得
 * @return							シングルトンのポインタ
 */
/************************************************************************************************/
KsGameConfigManager* KsGameConfigManager::getInstancePtr()
{
	return m_pInstance;
}

/************************************************************************************************/
/*
 * セットアップ処理する
 */
/************************************************************************************************/
void KsGameConfigManager::setup()
{
	if( !m_pInstance )
	{
		ksNew KsGameConfigManager();
	}
}

/************************************************************************************************/
/*
 * 後処理処理する
 */
/************************************************************************************************/
void KsGameConfigManager::shutdown()
{
	ksDELETE( m_pInstance );
}

/************************************************************************************************/
/*
 * コンストラクタ
 */
/************************************************************************************************/
KsGameConfigManager::KsGameConfigManager()
{
	create( ".\\Aria.exe.cfg" );
}

/************************************************************************************************/
/*
 * デストラクタ
 */
/************************************************************************************************/
KsGameConfigManager::~KsGameConfigManager()
{
	destroy();
}

/************************************************************************************************/
/*
 * 設定ファイルを読み込む
 * @param	rFileName	ファイルパック
 * @retval	ksTRUE		成功
 * @retval	ksFALSE		失敗
 */
/************************************************************************************************/
KsBool KsGameConfigManager::create( const KsString& rFileName )
{
	return ksTRUE;
}

