/************************************************************************************************/
/*
 * コンストラクタ
 */
/************************************************************************************************/
KsKinematicMotionState::KsKinematicMotionState( const KsMatrix4x4& refTransform, const KsMatrix4x4& refOffset, KsBone* pBone )
	: KsMotionState()
	, m_pMotionState( 0 )
{
	m_pMotionState = new btKinematicMotionState( refTransform, refOffset, pBone );
}

/************************************************************************************************/
/*
 * デストラクタ
 */
/************************************************************************************************/
KsKinematicMotionState::~KsKinematicMotionState()
{
	ksDELETE( m_pMotionState );
}

/************************************************************************************************/
/*
 * リソースを取得する
 * @return							 リソース
 */
/************************************************************************************************/
void* KsKinematicMotionState::getMotionStateResource()
{
	return m_pMotionState;
}

