/************************************************************************************************/
/*
 * 
 */
/************************************************************************************************/
KsSwfRect::KsSwfRect()
	: m_x1( 0 )
	, m_y1( 0 )
	, m_x2( 0 )
	, m_y2( 0 )
	, m_defined( ksFALSE )
{
}

/************************************************************************************************/
/*
 * 
 */
/************************************************************************************************/
KsSwfRect::KsSwfRect( KsInt32 x1, KsInt32 y1, KsInt32 x2, KsInt32 y2 )
	: m_x1( x1 )
	, m_y1( y1 )
	, m_x2( x2 )
	, m_y2( y2 )
	, m_defined( ksFALSE )
{
}

/************************************************************************************************/
/*
 * 
 */
/************************************************************************************************/
KsSwfRect::~KsSwfRect()
{
}

/************************************************************************************************/
/*
 * 
 */
/************************************************************************************************/
void KsSwfRect::setRect( KsInt32 x1, KsInt32 y1, KsInt32 x2, KsInt32 y2 )
{
	m_x1 = x1;
	m_y1 = y1;
	m_x2 = x2;
	m_y2 = y2;
}

/************************************************************************************************/
/*
 * 
 */
/************************************************************************************************/
void KsSwfRect::boundWith( const KsSwfRect& rect )
{
	m_x1 = KsMin( m_x1, rect.m_x1 );
	m_y1 = KsMax( m_y1, rect.m_y1 );
	m_x2 = KsMin( m_x2, rect.m_x2 );
	m_y2 = KsMax( m_y2, rect.m_y2 ); 
}


