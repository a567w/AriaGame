#include "KsFlashIDFactory.h"

/*==============================================================================================*/
/*                                     << 宣言 >>                                               */
/*==============================================================================================*/
KsUInt16 KsFlashIDFactory::s_characterID = 0;

/*==============================================================================================*/
/*                                     << 定義 >>                                               */
/*==============================================================================================*/


/************************************************************************************************/
/*
 * Charactor ID を取得する
 * @return		Charactor ID
 */
/************************************************************************************************/
KsUInt16 KsFlashIDFactory::getCharacterID() 
{ 
	KsUInt16	characterID = s_characterID; 
	s_characterID++; 
	return		characterID; 
}

/************************************************************************************************/
/*
 * リセットする( 0 に設定される)
 */
/************************************************************************************************/
void KsFlashIDFactory::reset()
{
	s_characterID = 0;
}


